  const missing = new Set();
  for (const [key, value] of Object.entries(envOverrides)) {
    if (Object.prototype.hasOwnProperty.call(process.env, key)) {
      previous.set(key, process.env[key]);
    } else {
      missing.add(key);
    }
    process.env[key] = String(value);
  }

